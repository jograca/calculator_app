package com.lmig.gfc.calculator.models;

public class Calculator {

	public int addValues(int firstNumber, int secondNumber) {
		return firstNumber + secondNumber;
	}
	
	public int subtractValues(int firstNumber, int secondNumber) {
		return firstNumber - secondNumber;
	}
	
	public int multiplyValues(int firstNumber, int secondNumber) {
		return firstNumber * secondNumber;
	}
	
	public int divideValues(int firstNumber, int secondNumber) {
		return firstNumber / secondNumber;
	}
	
	public int quotientValues(int firstNumber, int secondNumber) {
		return firstNumber ^ secondNumber;
	}
}
